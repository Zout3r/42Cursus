#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	size_t		count;

	count = 0;
	while (count < n)
	{
		if (*((unsigned char *)str1 + count)
			== *((unsigned char *)str2 + count))
		{
			count++;
		}
		else
		{
			return ((*((unsigned char *)str1 + count))
				- *((unsigned char *)str2 + count));
		}
	}
	return (0);
}

